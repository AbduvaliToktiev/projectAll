package school;

public class Classes {
    private Integer id;
    private Integer classNumber;
    private String classLetter;
    private String headTeacherFio;
    private Integer schoolId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getClassNumber() {
        return classNumber;
    }

    public void setClassNumber(Integer classNumber) {
        this.classNumber = classNumber;
    }

    public String getClassLetter() {
        return classLetter;
    }

    public void setClassLetter(String classLetter) {
        this.classLetter = classLetter;
    }

    public String getHeadTeacherFio() {
        return headTeacherFio;
    }

    public void setHeadTeacherFio(String headTeacherFio) {
        this.headTeacherFio = headTeacherFio;
    }

    public Integer getSchoolId() {
        return schoolId;
    }

    public void setSchoolId(Integer schoolId) {
        this.schoolId = schoolId;
    }
}
